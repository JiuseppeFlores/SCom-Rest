        $usuario->apellidoMaterno = $request->apellidoMaterno;
        $usuario->estado = $request->estado;

        $usuario->save();
        $data = array('response' => 'true','data' => $usuario);
        return $data;
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
